def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  diff = num1 - num2
  diff
end

def division(num1, num2)
  quot = num1 / num2
  quot
end

def multiplication(num1, num2)
  product = num1 * num2
  product
end

def modulo(num1, num2)
  remain = num1 % num2
  remain
end

def square_root(num)
  square = Math.sqrt(num)
  square
end